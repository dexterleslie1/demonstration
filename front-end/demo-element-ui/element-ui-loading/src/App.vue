<template>
  <div id="app">
    <button @click="handleClickLoading()">弹出加载中提示</button>
  </div>
</template>

<script>
export default {
  methods: {
    handleClickLoading() {
      const loading = this.$loading({
        lock: true,
        text: '加载中...',
        spinner: 'el-icon-loading',
        background: 'rgba(0, 0, 0, 0.7)'
      });
      setTimeout(() => {
        loading.close()
      }, 2000);
    }
  }
}
</script>

